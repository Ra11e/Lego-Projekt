package FunctionLayer;


public class WallDetail {
    private int length, height;
    
    public WallDetail(int length, int height) {
        this.length = length;
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public int getHeight() {
        return height;
    }
}
